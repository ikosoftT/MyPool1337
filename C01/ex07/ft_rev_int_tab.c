void        ft_rev_int_tab(int * tab,int size)
{
    int     i;
    int     a;
    int     b;

    a = 0;
    b = size - 1;
    i = 0;
    while (i < (size / 2)) {
        int t = tab[a];
        tab[a] = tab[b];
        tab[b] = t;
        b--;
        a++;
        i++;
    }
}

